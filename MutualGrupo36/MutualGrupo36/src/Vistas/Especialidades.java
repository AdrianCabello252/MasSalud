    public void borrarFila(){
        int f=jtTabla.getRowCount()-1;
        for(;f>=0;f--){
            modelo.removeRow(f);
        }
    }
    
    private void armarCabecera(){
    ArrayList<Object> filaCabecera=new ArrayList<>();
    modelo.addColumn("Nombre");
    for(Object it:filaCabecera){
        modelo.addColumn(it);
    }
    jtTabla.setModel(modelo);
}
    
