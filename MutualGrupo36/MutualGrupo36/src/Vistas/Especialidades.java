    private void jtNombreKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtNombreKeyReleased
            EspecialidadData data=new EspecialidadData();
            String nombre=jtNombre.getText();
            int longi=nombre.length();
            char caracter=nombre.charAt(longi-1);
                if(Character.isLetter(caracter)){
                borrarFila();
                for(Especialidad especi:listaE){
                    if(especi.getTipoEspecialidad().toLowerCase().startsWith(jtNombre.getText())){
                        modelo.addRow(new Object[]{especi.getTipoEspecialidad()
                        });
                    }                   
                }
            }else{
                    JOptionPane.showMessageDialog(null, "caracter incorrecto");
                    cargarTabla();
                }              
                
    }//GEN-LAST:event_jtNombreKeyReleased

